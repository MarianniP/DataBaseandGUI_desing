    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection myConn= DriverManager.getConnection("jdbc:mysql://localhost/datab_etaireia?useUnicode=true&useJDBCCompliantTimezoneShift=true&useLegacyDatetimeCode=false&serverTimezone=UTC", "root","root");
            
            String sql = "select name,password,surname,reg_date,email,exp_years,firm from recruiter inner join user on user.usrname=recruiter.username where user.usrname = ? ";
            PreparedStatement pstmt = myConn.prepareStatement(sql);
            pstmt.setString(1,jLabel2.getText());
            
            
            ResultSet rs = pstmt.executeQuery();
            
            if(rs.next()){
                passf.setText(rs.getString("password"));
                namef.setText(rs.getString("name"));
                snamef.setText(rs.getString("surname"));
                emailf.setText(rs.getString("email"));
                reg_datef.setText(rs.getString("reg_date"));
                yoef.setText(rs.getString("exp_years"));
                firmf.setText(rs.getString("firm"));
            }
            else{
                JOptionPane.showMessageDialog(null,"Something went wrong!");
            }
            myConn.close();
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

